      methodChannelWithName:@"flutter_minemap"
            binaryMessenger:[registrar messenger]];
  FlutterMinemapPlugin* instance = [[FlutterMinemapPlugin alloc] init];
  [registrar addMethodCallDelegate:instance channel:channel];
}

